// if , else if, else

void main(List<String> args) {
  const yourName = 'Coco';
  const myName = 'Ginger';
  if (yourName == myName) {
    print('We have same name!');
  } else if (yourName == 'Coco') {
    print('Hello Coco');
  } else if (myName == 'Ginger') {
    print('Hello Ginger');
  } else {
    print('Who are you?');
  }
}
